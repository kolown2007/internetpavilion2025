  <div class="max-w-[600px]">
    <h1 class="text-2xl font-semibold mb-6">About the Project</h1>
    
    <p class="mb-4">
      <span class="italic">Iterations (Online)</span> is a continuation of the printed work <span class="italic">Iterations</span>, first shared as a takeaway piece in my solo exhibition <span class="italic">Parallax, A Trace</span> (MO_Space, BGC, 2025)
    </p>
    
    <p class="mb-4">
      This version lives online, fragmented, fleeting, and open to anyone. It allows visitors to generate a unique text fragment drawn from a private poem. The original text is never shown in full. Instead, a Markov chain system reshapes its language into new combinations.
    </p>
    
    <p class="mb-4">
      Each fragment is a partial echo, something remembered but not quite whole.<br>
      Some lines arrive with clarity. Others remain hazy, like something recalled halfway.
    </p>
    
    <p class="mb-4">
      The project reflects the recurring themes explored in <span class="italic">Parallax, A Trace</span>: how light, memory, emotion, and perception shift across spaces, bodies, and technologies.<br>
      It invites viewers to experience the scattering and reframing of language through digital traces.
    </p>
    
    <p class="mb-6">
      You are invited to sit with what you receive.<br>
      You may choose to save it or let it drift.
    </p>
    
    <p class="italic mb-8">
      A trace never repeated.<br>
      A poem you'll never fully read.
    </p>
    
    <hr class="border-amber-800 my-8">
    
    <h2 class="text-lg font-medium mb-3">Note</h2>
    
    <p class="text-sm italic">
      A total of 82 fragments were printed and shared as takeaways during the exhibition. Some of those lines may reappear here, though the printed set was limited and is no longer in circulation. Any recurrence is intentional. It reflects how memory re-emerges, sometimes partially, sometimes familiar, always transformed.
    </p>
